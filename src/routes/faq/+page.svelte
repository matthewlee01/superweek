<div>
  <h1>faq</h1>
  <h5>(frequently asked questions)</h5>

  <p class="q">who will be at this retreat?</p>
  <p>
    4 churches from vancouver + toronto:<br />
    - <a href="https://lhf.ca/" target="_blank">living hope fellowship</a><br />
    -
    <a href="https://bridgewaychurch.ca/" target="_blank">bridgeway church</a
    ><br />
    - <a href="https://www.coah.ca/" target="_blank">city on a hill </a><br />
    - <a href="https://vccem.com/" target="_blank">vaughan community church</a>
  </p>

  <p class="q">what should i pack?</p>
  <p>
    the hotel will provide the usual hotel stuff (bedding, towels, shampoo,
    soap, etc.), so bring everything else. if you like taking notes, bring a
    notebook and a writing instrument. you should also bring your bible or bible
    app.
  </p>

  <p class="q">where are we meeting?</p>
  <p>
    we will be sleeping at the <a
      href="https://maps.app.goo.gl/RZ2bZuPu8UyMKa657"
      target="_blank">best western plus burnaby (5411 kingway st)</a
    >
    , but almost everything else will start and end at the
    <a href="https://maps.app.goo.gl/VtvjcqQi6qXmveNJ6" target="_blank">
      royal oak ministry centre (7175 royal oak ave)</a
    >, which is where LHF meets on sundays.
  </p>
  <p>
    note: we want to be good witnesses to our neighbours and therefore ask that
    everyone remains inside the church building or back at the hotel as of 10pm.
  </p>
  <p>
    the hotel is about a 10 min. walk from the church building, and some locals
    will be driving back and forth each day.
  </p>

  <p class="q">what will we be eating?</p>
  <p>
    breakfast is provided by the hotel. all other meals will be catered. if you
    have dietary concerns, please notify us via email (<a
      href="mailto:office@lhf.ca">office@lhf.ca</a
    >).
  </p>

  <p class="q">what is “HS” and “B+Y”?</p>
  <p>
    we have two age groups attending super week: <br />
    - HS: just finished gr. 10-12 <br />
    - B+Y: roughly 19-30ish (post-secondary in 2023).
  </p>

  <p class="q">what is “B+Y”?</p>
  <p>
    it’s short for “BAYM + YAM” which stands for:<br />
    - “becoming a young adult ministry” - about 18-22ish<br />
    - “young adult ministry” - about 23-30ish
  </p>

  <p class="q">shouldn’t “becoming a young adult ministry” be “BYAM”?</p>
  <p>yeah, probably, but it’s not as fun to say.</p>

  <p class="q">can i go off site during super week?</p>
  <p>
    though we cannot and do not want to police everyone, we are asking everyone
    to stay at the hotel or church building except for emergencies. so long as
    you are not being manipulative or sneaky, we are reasonable, so let’s talk.
    if you are in HS, you are not allowed to go off site without permission.
  </p>

  <p class="q">what will we do during the organized free time?</p>
  <p>
    there will be a mix of indoor and outdoor activities. if you plan on being
    outside, please bring sunblock and anything else you need to prevent skin
    cancer and eye damage.
  </p>

  <p class="q">who is my point of contact?</p>
  <p>
    make sure you have your pastor, your small group leader, and your exposure
    trip leader in your phone.
  </p>

  <p class="q">how should i prepare?</p>
  <p>pray that all of us would be open to what God might want to show us.</p>
